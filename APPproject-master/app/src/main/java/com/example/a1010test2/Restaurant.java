package com.example.a1010test2;

public class Restaurant {
    private String R_profile;
    private String R_name;
    private Float R_score;
    private String R_address;
    private String R_number;
    private String R_latitude;
    private String R_longitude;
    public Restaurant(){}

    public String getR_profile() {
        return R_profile;
    }

    public void setR_profile(String profile) {
        this.R_profile = profile;
    }

    public String getR_name() {
        return R_name;
    }

    public void setR_name(String name) {
        this.R_name = name;
    }

    public Float getR_score() {
        return R_score;
    }

    public void setR_score(Float score) {
        this.R_score = score;
    }

    public String getR_address() {
        return R_address;
    }

    public void setR_address(String address) {
        this.R_address = address;
    }

    public String getR_number() {
        return R_number;
    }

    public void setR_number(String number) {
        this.R_number = number;
    }

    public String getR_latitude() {
        return R_latitude;
    }

    public void setR_latitude(String latitude) {
        this.R_latitude = latitude;
    }

    public String getR_longitude() {
        return R_longitude;
    }

    public void setR_longitude(String longitude) {
        this.R_longitude = longitude;
    }

}
