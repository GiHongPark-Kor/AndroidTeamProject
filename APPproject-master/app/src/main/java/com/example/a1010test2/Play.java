package com.example.a1010test2;

public class Play {
    private String P_profile;
    private String P_name;
    private Float P_score;
    private String P_address;
    private String P_number;
    private String P_latitude;
    private String P_longitude;
    public Play(){}

    public String getP_profile() {
        return P_profile;
    }

    public void setP_profile(String profile) {
        this.P_profile = profile;
    }

    public String getP_name() {
        return P_name;
    }

    public void setP_name(String name) { this.P_name = name; }

    public Float getP_score() {
        return P_score;
    }

    public void setP_score(Float score) {
        this.P_score = score;
    }
    public String getP_address() {
        return P_address;
    }

    public void setP_address(String address) {
        this.P_address = address;
    }

    public String getP_number() {
        return P_number;
    }

    public void setP_number(String number) {
        this.P_number = number;
    }

    public String getP_latitude() {
        return P_latitude;
    }

    public void setP_latitude(String latitude) {
        this.P_latitude = latitude;
    }

    public String getP_longitude() {
        return P_longitude;
    }

    public void setP_longitude(String longitude) {
        this.P_longitude = longitude;
    }

}
