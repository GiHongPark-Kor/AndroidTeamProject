package com.example.a1010test2;

public class Member {
    private String M_profile;
    private String M_name;
    private String M_address;
    private String M_number;
    private String M_user;
    private String M_latitude;
    private String M_longitude;
    private String M_score;
    private String M_temp;
    public Member(){}

    public String getM_user() {
        return M_user;
    }

    public void setM_user(String user) {
        this.M_user = user;
    }

    public String getM_profile() {
        return M_profile;
    }

    public void setM_profile(String profile) {
        this.M_profile = profile;
    }

    public String getM_name() {
        return M_name;
    }

    public void setM_name(String name) {
        this.M_name = name;
    }

    public String getM_score() {
        return M_score;
    }

    public void setM_score(String score) {
        this.M_score = score;
    }

    public String getM_address() {
        return M_address;
    }

    public void setM_address(String address) {
        this.M_address = address;
    }

    public String getM_number() {
        return M_number;
    }

    public void setM_number(String number) {
        this.M_number = number;
    }

    public String getM_latitude() {
        return M_latitude;
    }

    public void setM_latitude(String latitude) {
        this.M_latitude = latitude;
    }

    public String getM_longitude() {
        return M_longitude;
    }

    public void setM_longitude(String longitude) {
        this.M_longitude = longitude;
    }

    public String getM_temp() {
        return M_temp;
    }

    public void setM_temp(String temp) {
        this.M_temp = temp;
    }


}
