package com.example.a1010test2;

public class Search {
    private String S_profile;
    private String S_name;
    private String S_address;
    private String S_number;
    private Float S_score;
    private String S_latitude;
    private String S_longitude;


    public Search(){}

    public String getS_profile() {
        return S_profile;
    }

    public void setS_profile(String profile) {
        this.S_profile = profile;
    }

    public String getS_name() {
        return S_name;
    }

    public void setS_name(String name) {
        this.S_name = name;
    }

    public Float getS_score() {
        return S_score;
    }

    public void setS_score(Float score) {
        this.S_score = score;
    }

    public String getS_address() {
        return S_address;
    }

    public void setS_address(String address) {
        this.S_address = address;
    }

    public String getS_number() {
        return S_number;
    }

    public void setS_number(String number) {
        this.S_number = number;
    }

    public String getS_latitude() {
        return S_latitude;
    }

    public void setS_latitude(String latitude) {
        this.S_latitude = latitude;
    }

    public String getS_longitude() {
        return S_longitude;
    }

    public void setS_longitude(String longitude) {
        this.S_longitude = longitude;
    }

}
