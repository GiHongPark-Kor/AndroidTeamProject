package com.example.a1010test2;

public class Cafe {
    private String C_profile;
    private String C_name;
    private String C_address;
    private String C_number;
    private Float C_score;
    private String C_latitude;
    private String C_longitude;


    public Cafe(){}

    public String getC_profile() {
        return C_profile;
    }

    public void setC_profile(String profile) {
        this.C_profile = profile;
    }

    public String getC_name() {
        return C_name;
    }

    public void setC_name(String name) {
        this.C_name = name;
    }

    public Float getC_score() {
        return C_score;
    }

    public void setC_score(Float score) {
        this.C_score = score;
    }

    public String getC_address() {
        return C_address;
    }

    public void setC_address(String address) {
        this.C_address = address;
    }

    public String getC_number() {
        return C_number;
    }

    public void setC_number(String number) {
        this.C_number = number;
    }

    public String getC_latitude() {
        return C_latitude;
    }

    public void setC_latitude(String latitude) {
        this.C_latitude = latitude;
    }

    public String getC_longitude() {
        return C_longitude;
    }

    public void setC_longitude(String longitude) {
        this.C_longitude = longitude;
    }

}
